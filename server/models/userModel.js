import mongoose from "mongoose";

const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    phone: {
      type: Number,
      required: true,
    },
    email_verification_status:{
    type:Boolean,
    default:0,
   },
   
  },
  { timestamps: true }
);

export default mongoose.model("User", userSchema);
